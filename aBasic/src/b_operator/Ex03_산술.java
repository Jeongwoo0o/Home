package b_operator;

/*
 * 	산술 연산자
 * 
 * 		*	/	+	-	%
 */

public class Ex03_산술 {
	public static void main(String[] args) {

		int a = 3;
		
		//	a / 2일 때, 나머지 == 0 짝수, 나머지 != 0 홀수
		if(a % 2 != 0) {
			System.out.println("홀수");
		} else {
			System.out.println("짝수");
		}

	}
}
















































