package b_operator;

public class Ex10_대입 {
	public static void main(String[] args) {

		int a = 10;
		int b = 7;
		
		//a = a + b;
		a += b;
		System.out.println(a);	//17
		
		//a = a - b;
		a -= b;
		System.out.println(a);	//10
		
		//a = a * b;
		a *= b;
		System.out.println(a);	//70
		
		//a = a % b;
		a %= b;
		System.out.println(a);	//0
		
		//a = a / b;
		a /= b;
		System.out.println(a);	//0

	}
}












































