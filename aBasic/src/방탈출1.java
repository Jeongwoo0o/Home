
public class 방탈출1 {
	public static void main(String[] args) {

		int st = 0;
		for(int i = 1; i <= 10000; i++) {
			int num = i;
			
			while(num != 0) {
				int na = num % 10;
				if(na == 8) {
					st += 1;
				}	//	end if(na)
				num /= 10;
			}	//	end while(num)
		}	//	end for(i)
		System.out.println(st);
	}
}







































